let currentBookIndex = 0; // Default: Kitab Pertama

document.addEventListener("DOMContentLoaded", () => {
    initializeSidebar();
    renderContent(currentBookIndex);
    setupScrollButton();
});

function initializeSidebar() {
    const sidebarList = document.getElementById('chapter-list');
    sidebarList.innerHTML = ''; // Bersihkan daftar lama

    libraryData.forEach((book, index) => {
        const li = document.createElement('li');
        li.innerText = book.title;
        
        // Set active class untuk item pertama
        if (index === 0) li.classList.add('active');

        li.onclick = () => {
            // Hapus class active dari semua item
            document.querySelectorAll('#chapter-list li').forEach(item => item.classList.remove('active'));
            // Tambah class active ke item yang diklik
            li.classList.add('active');
            
            // Render konten buku yang dipilih
            currentBookIndex = index;
            renderContent(index);
            
            // Scroll ke atas agar nyaman
            window.scrollTo({ top: 0, behavior: 'smooth' });
            
            // Tutup sidebar di HP (Opsional jika nanti ada toggle)
            if(window.innerWidth <= 768) {
                // document.querySelector('.sidebar').style.display = 'none'; // Jika ingin auto-close
            }
        };

        sidebarList.appendChild(li);
    });
    
    // Hapus elemen "Coming Soon" statis karena sekarang semua kitab sudah lengkap!
    // (Opsional: Jika ingin menambah info lain di sidebar bisa di sini)
}

function renderContent(index) {
    const wrapper = document.getElementById('content-wrapper');
    const bookData = libraryData[index];
    
    // Efek Fade Out
    wrapper.style.opacity = '0';
    
    setTimeout(() => {
        wrapper.innerHTML = ''; // Kosongkan konten saat ini
        
        // Buat Judul Utama Kitab
        const mainSection = document.createElement('div');
        mainSection.className = 'kitab-section';
        
        const title = document.createElement('h2');
        title.className = 'kitab-title';
        title.innerText = bookData.title;
        mainSection.appendChild(title);

        // Loop untuk membuat Fasal (Sub-bab)
        bookData.sections.forEach((section) => {
            const fasalGroup = document.createElement('div');
            fasalGroup.className = 'fasal-group';
            
            // Badge "FASAL"
            const badge = document.createElement('span');
            badge.className = 'fasal-badge';
            badge.innerText = "FASAL";
            
            // Judul Fasal (Arab)
            const fasalTitle = document.createElement('h3');
            fasalTitle.style.fontFamily = "var(--arabic-font)";
            fasalTitle.style.fontSize = "1.8rem";
            fasalTitle.style.color = "var(--accent-color)";
            fasalTitle.style.textAlign = "right"; // Kanan ke Kiri (RTL)
            fasalTitle.style.marginBottom = "15px";
            fasalTitle.innerText = section.title;

            // Teks Konten (Arab)
            const p = document.createElement('p');
            p.className = 'arabic-content';
            p.innerText = section.text;

            fasalGroup.appendChild(badge);
            fasalGroup.appendChild(fasalTitle);
            fasalGroup.appendChild(p);
            mainSection.appendChild(fasalGroup);
        });

        wrapper.appendChild(mainSection);
        
        // Update penanda akhir halaman
        const endMark = document.querySelector('.end-mark p');
        if(endMark) {
            endMark.innerText = `End of ${bookData.title}`;
        }

        // Efek Fade In
        wrapper.style.opacity = '1';
    }, 200);
}

// Logika Tombol Kembali ke Atas (Scroll Top)
function setupScrollButton() {
    const btn = document.getElementById("scrollTopBtn");
    
    window.onscroll = function() {
        if (document.body.scrollTop > 300 || document.documentElement.scrollTop > 300) {
            btn.style.display = "block";
        } else {
            btn.style.display = "none";
        }
    };
    
    btn.onclick = function() {
        window.scrollTo({ top: 0, behavior: 'smooth' });
    };
}

// Logika Modal (About Us)
function openAbout() {
    document.getElementById("aboutModal").style.display = "block";
}

function closeAbout() {
    document.getElementById("aboutModal").style.display = "none";
}

// Tutup modal jika klik di luar area konten
window.onclick = function(event) {
    const modal = document.getElementById("aboutModal");
    if (event.target == modal) {
        modal.style.display = "none";
    }
}
